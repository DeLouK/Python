# Script que suma dos numeros e imprime su total
a = 25
b = 14.2

# Primera forma
print(a+b)

# Segunda forma
print("a+b=")
print("a+b")

# Tercera forma
print("a+b =", a+b)

# Cuarta forma: usando una variable para almacenar la suma
total = a + b
print(total)
print("a+b =",total)
print(f"a + b = {a+b}")